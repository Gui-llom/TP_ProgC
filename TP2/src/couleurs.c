#include <stdio.h>

struct Couleur {
    unsigned char r, g, b, a;
};

int main() {
    struct Couleur tab[10] = {
        {0xef, 0x78, 0x12, 0xff},
        {0x2c, 0xc8, 0x64, 0xff},
        {0x12, 0x34, 0x56, 0xff},
        {0xaa, 0xbb, 0xcc, 0xff},
        {0x10, 0x20, 0x30, 0xff},
        {0x80, 0x90, 0xa0, 0xff},
        {0xff, 0x00, 0x00, 0xff},
        {0x00, 0xff, 0x00, 0xff},
        {0x00, 0x00, 0xff, 0xff},
        {0x77, 0x88, 0x99, 0xff}
    };

    for(int i = 0; i < 10; i++) {
        printf("\nCouleur %d :\n", i+1);
        printf("Rouge : %u\n", tab[i].r);
        printf("Vert  : %u\n", tab[i].g);
        printf("Bleu  : %u\n", tab[i].b);
        printf("Alpha : %u\n", tab[i].a);
    }

    return 0;
}
